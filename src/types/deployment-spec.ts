/**
 * DeploymentSpec - Single source of truth for DogeOS deployments
 *
 * This specification is used by AI agents and deployment scripts to:
 * 1. Generate config.toml
 * 2. Generate doge-config.toml
 * 3. Generate setup_defaults.toml
 * 4. Generate values/*.yaml for Helm charts
 * 5. Validate deployment configurations
 */

export interface DeploymentSpec {
  /** Account and key configuration */
  accounts: AccountsConfig

  /** Bridge configuration */
  bridge: BridgeConfig

  /** Celestia DA configuration */
  celestia: CelestiaConfig

  /** Contract deployment configuration */
  contracts: ContractsConfig

  /** Optional da-publisher service configuration */
  daPublisher?: DaPublisherConfig

  /** Database configuration */
  database: DatabaseConfig

  /** Dogecoin L1 configuration */
  dogecoin: DogecoinConfig

  /** Optional fee-oracle service configuration */
  feeOracle?: FeeOracleConfig

  /** Frontend and ingress configuration */
  frontend: FrontendConfig

  /** Genesis configuration */
  genesis: GenesisConfig

  /** Optional container image overrides for services */
  images?: ImagesConfig

  /** Infrastructure configuration */
  infrastructure: InfrastructureConfig

  /** Optional l1-interface service configuration */
  l1Interface?: L1InterfaceConfig

  /** Deployment metadata */
  metadata: DeploymentMetadata

  /** Network and chain configuration */
  network: NetworkConfig

  /** Rollup parameters */
  rollup: RollupConfig

  /** Signing configuration */
  signing: SigningConfig

  /** Optional test/development settings */
  test?: TestConfig

  /** Schema version for forward compatibility */
  version: '1.0'

  /** Optional withdrawal-processor service configuration */
  withdrawalProcessor?: WithdrawalProcessorConfig
}

export interface DeploymentMetadata {
  /** Optional description */
  description?: string

  /** Deployment environment: mainnet, testnet, devnet */
  environment: 'devnet' | 'mainnet' | 'testnet'

  /** Human-readable deployment name */
  name: string

  /** Optional deployment owner/contact */
  owner?: string

  /** Optional tags for organization */
  tags?: Record<string, string>
}

export interface InfrastructureConfig {
  /** AWS-specific configuration */
  aws?: {
    accountId: string
    eksClusterName: string
    region: string
    /** Secrets Manager prefix */
    secretsPrefix?: string
  }

  /** Number of bootnode instances */
  bootnodeCount: number

  /** Bootnode instance configurations (generated by gen-keystore or provided) */
  bootnodes?: BootnodeInstanceConfig[]

  /** GCP-specific configuration */
  gcp?: {
    gkeClusterName: string
    project: string
    region: string
    /** Secret Manager project (defaults to project if not set) */
    secretsProject?: string
  }

  /** Local development configuration */
  local?: {
    /** Use Kubernetes secrets directly instead of external secrets operator */
    useK8sSecrets?: boolean
  }

  /** Kubernetes namespace */
  namespace?: string

  /** Cloud provider: aws, gcp, local */
  provider: 'aws' | 'gcp' | 'local'

  /** Number of sequencer instances */
  sequencerCount: number

  /** Sequencer instance configurations (generated by gen-keystore or provided) */
  sequencers?: SequencerInstanceConfig[]
}

/** Configuration for a single sequencer instance */
export interface SequencerInstanceConfig {
  /** Enode URL for P2P peering */
  enodeUrl?: string
  /** Instance index (0, 1, 2, ...) */
  index: number
  /** Signer address derived from keystore */
  signerAddress: string
}

/** Configuration for a single bootnode instance */
export interface BootnodeInstanceConfig {
  /** Enode URL for P2P peering */
  enodeUrl?: string
  /** Instance index (0, 1, 2, ...) */
  index: number
  /** Public IP or hostname for external P2P (optional) */
  publicEndpoint?: string
}

export interface NetworkConfig {
  /** Beacon RPC endpoint (for Celestia) */
  beaconRpcEndpoint?: string

  /** DA Publisher endpoint */
  daPublisherEndpoint: string

  /** L1 chain ID */
  l1ChainId: number

  /** L1 chain name (displayed in UIs) */
  l1ChainName: string

  /** Internal L1 RPC endpoint (cluster-internal) */
  l1RpcEndpoint: string

  /** Internal L1 WebSocket endpoint */
  l1RpcEndpointWebsocket?: string

  /** L2 chain ID */
  l2ChainId: number

  /** L2 chain name (displayed in UIs) */
  l2ChainName: string

  /** Internal L2 RPC endpoint */
  l2RpcEndpoint: string

  /** Native token symbol */
  tokenSymbol: string
}

export interface DatabaseConfig {
  /** Database admin connection for initialization */
  admin?: {
    database: string
    host: string
    /** Use $ENV:VAR_NAME to reference environment variable */
    password: string
    port: number
    username: string
    /** VPC/internal host if different from public */
    vpcHost?: string
    vpcPort?: number
  }

  /** Database user credentials per service */
  credentials: {
    adminSystemPassword: string
    blockscoutPassword: string
    bridgeHistoryPassword: string
    chainMonitorPassword: string
    coordinatorPassword: string
    gasOraclePassword: string
    l1ExplorerPassword?: string
    rollupExplorerPassword: string
    /** Use $ENV:VAR_NAME pattern for secrets */
    rollupNodePassword: string
  }

  /** Individual service database names (auto-created) */
  databases: {
    adminSystem: string
    blockscout: string
    bridgeHistory: string
    chainMonitor: string
    coordinator: string
    gasOracle: string
    l1Explorer?: string
    rollupExplorer: string
    rollupNode: string
  }
}

export interface AccountsConfig {
  /** Deployer account (deploys all contracts) */
  deployer: {
    address: string
    /** Use $ENV:VAR_NAME for private key */
    privateKey: string
  }

  /** L1 commit sender (submits batch commitments) */
  l1CommitSender: {
    address: string
    privateKey: string
  }

  /** L1 finalize sender (finalizes batches) */
  l1FinalizeSender: {
    address: string
    privateKey: string
  }

  /** L1 gas oracle sender */
  l1GasOracleSender: {
    address: string
    privateKey: string
  }

  /** L2 gas oracle sender */
  l2GasOracleSender: {
    address: string
    privateKey: string
  }

  /** Owner account (owns upgradeable contracts) */
  owner: {
    address: string
  }
}

export interface DogecoinConfig {
  /** Optional Blockbook API for UTXO queries */
  blockbook?: {
    apiKey?: string
    apiUrl: string
  }

  /** Block height to start indexing from */
  indexerStartHeight: number

  /** Network type */
  network: 'mainnet' | 'testnet'

  /** RPC connection */
  rpc: {
    /** Use $ENV:VAR_NAME for password */
    password: string
    url: string
    username: string
  }

  /** Wallet file path */
  walletPath: string
}

export interface CelestiaConfig {
  /** Auth token for Celestia node */
  authToken?: string

  /** Block height to start indexing from */
  indexerStartBlock: number

  /** Mnemonic for Celestia account - use $ENV:VAR_NAME */
  mnemonic: string

  /** DA namespace (hex) */
  namespace: string

  /** Signer address for DA submissions */
  signerAddress: string

  /** Tendermint RPC URL */
  tendermintRpcUrl: string
}

export interface BridgeConfig {
  /** Required confirmations for deposits */
  confirmationsRequired: number

  /** Fee rate in satoshis per kvB */
  feeRateSatPerKvb: number

  /** Fee recipient address */
  feeRecipient: string

  /** Deposit and withdrawal fees in wei */
  fees: {
    deposit: string
    minWithdrawalAmount: string
    withdrawal: string
  }

  /** Key counts */
  keyCounts: {
    attestation: number
    correctness: number
    recovery: number
  }

  /** Seed string for deterministic key derivation */
  seedString: string

  /** Target funding amounts in satoshis */
  targetAmounts: {
    bridge: number
    feeWallet: number
    sequencer: number
  }

  /** Threshold settings */
  thresholds: {
    attestation: number
    correctness: number
    recovery: number
    sequencer: number
  }

  /** Timelock for recovery operations (seconds) */
  timelockSeconds: number
}

export interface SigningConfig {
  /** AWS KMS configuration */
  awsKms?: {
    accountId: string
    networkAlias: string
    region: string
    /** Key suffixes for different signers */
    suffixes: string[]
  }

  /** CubeSigner configuration */
  cubesigner?: {
    /** Role configurations from cubesigner-init */
    roles: Array<{
      keys: Array<{
        keyId: string
        keyType: string
        materialId: string
        publicKey: string
      }>
      name: string
      roleId: string
    }>
  }

  /** Local signer configuration */
  local?: {
    /** Signer instances */
    signers: Array<{
      index: number
      port: number
      /** Private key - use $ENV:VAR_NAME */
      privateKey?: string
    }>
  }

  /** Signing method */
  method: 'aws-kms' | 'cubesigner' | 'local'

  /** TSO service URL */
  tsoServiceUrl?: string
}


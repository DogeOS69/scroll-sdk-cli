/**
 * Container image configuration for a single service
 */
export interface ImageConfig {
  /** Image pull policy */
  pullPolicy?: 'Always' | 'IfNotPresent' | 'Never'

  /** Container registry/repository (e.g., scrolltech/l2geth, ghcr.io/org/image) */
  repository?: string

  /** Image tag (e.g., v5.8.36, latest). Supports $ENV:VAR_NAME pattern */
  tag?: string
}

/**
 * Container images configuration for all services
 * Allows overriding default images for testing/development
 */
export interface ImagesConfig {
  /** Default settings applied to all services */
  defaults?: {
    pullPolicy?: 'Always' | 'IfNotPresent' | 'Never'
  }

  /** Per-service image overrides */
  services?: {
    blockscout?: ImageConfig
    // Bridge Services
    bridgeHistoryApi?: ImageConfig
    bridgeHistoryFetcher?: ImageConfig

    chainMonitor?: ImageConfig
    coordinator?: ImageConfig
    cubesignerSigner?: ImageConfig
    daPublisher?: ImageConfig
    feeOracle?: ImageConfig
    // Frontend & Explorers
    frontends?: ImageConfig

    gasOracle?: ImageConfig
    // Infrastructure
    l1Interface?: ImageConfig
    l2Bootnode?: ImageConfig
    l2Rpc?: ImageConfig
    // L2 Execution Layer
    l2Sequencer?: ImageConfig

    rollupExplorerBackend?: ImageConfig
    // Rollup Services
    rollupRelayer?: ImageConfig
    tsoService?: ImageConfig

    withdrawalProcessor?: ImageConfig
  }
}


/**
 * DeploymentSpec - Single source of truth for DogeOS deployments
 *
 * This specification is used by AI agents and deployment scripts to:
 * 1. Generate config.toml
 * 2. Generate doge-config.toml
 * 3. Generate setup_defaults.toml
 * 4. Generate values/*.yaml for Helm charts
 * 5. Validate deployment configurations
 */

export interface DeploymentSpec {
  /** Schema version for forward compatibility */
  version: '1.0'

  /** Deployment metadata */
  metadata: DeploymentMetadata

  /** Infrastructure configuration */
  infrastructure: InfrastructureConfig

  /** Network and chain configuration */
  network: NetworkConfig

  /** Database configuration */
  database: DatabaseConfig

  /** Account and key configuration */
  accounts: AccountsConfig

  /** Dogecoin L1 configuration */
  dogecoin: DogecoinConfig

  /** Celestia DA configuration */
  celestia: CelestiaConfig

  /** Bridge configuration */
  bridge: BridgeConfig

  /** Signing configuration */
  signing: SigningConfig

  /** Frontend and ingress configuration */
  frontend: FrontendConfig

  /** Rollup parameters */
  rollup: RollupConfig

  /** Contract deployment configuration */
  contracts: ContractsConfig

  /** Genesis configuration */
  genesis: GenesisConfig

  /** Optional test/development settings */
  test?: TestConfig

  /** Optional container image overrides for services */
  images?: ImagesConfig
}

export interface DeploymentMetadata {
  /** Human-readable deployment name */
  name: string

  /** Deployment environment: mainnet, testnet, devnet */
  environment: 'mainnet' | 'testnet' | 'devnet'

  /** Optional description */
  description?: string

  /** Optional deployment owner/contact */
  owner?: string

  /** Optional tags for organization */
  tags?: Record<string, string>
}

export interface InfrastructureConfig {
  /** Cloud provider: aws, gcp, local */
  provider: 'aws' | 'gcp' | 'local'

  /** AWS-specific configuration */
  aws?: {
    region: string
    accountId: string
    eksClusterName: string
    /** Secrets Manager prefix */
    secretsPrefix?: string
  }

  /** GCP-specific configuration */
  gcp?: {
    project: string
    region: string
    gkeClusterName: string
    /** Secret Manager project (defaults to project if not set) */
    secretsProject?: string
  }

  /** Local development configuration */
  local?: {
    /** Use Kubernetes secrets directly instead of external secrets operator */
    useK8sSecrets?: boolean
  }

  /** Kubernetes namespace */
  namespace?: string

  /** Number of sequencer instances */
  sequencerCount: number

  /** Number of bootnode instances */
  bootnodeCount: number

  /** Sequencer instance configurations (generated by gen-keystore or provided) */
  sequencers?: SequencerInstanceConfig[]

  /** Bootnode instance configurations (generated by gen-keystore or provided) */
  bootnodes?: BootnodeInstanceConfig[]
}

/** Configuration for a single sequencer instance */
export interface SequencerInstanceConfig {
  /** Instance index (0, 1, 2, ...) */
  index: number
  /** Signer address derived from keystore */
  signerAddress: string
  /** Enode URL for P2P peering */
  enodeUrl?: string
}

/** Configuration for a single bootnode instance */
export interface BootnodeInstanceConfig {
  /** Instance index (0, 1, 2, ...) */
  index: number
  /** Enode URL for P2P peering */
  enodeUrl?: string
  /** Public IP or hostname for external P2P (optional) */
  publicEndpoint?: string
}

export interface NetworkConfig {
  /** L1 chain name (displayed in UIs) */
  l1ChainName: string

  /** L2 chain name (displayed in UIs) */
  l2ChainName: string

  /** L1 chain ID */
  l1ChainId: number

  /** L2 chain ID */
  l2ChainId: number

  /** Native token symbol */
  tokenSymbol: string

  /** Internal L1 RPC endpoint (cluster-internal) */
  l1RpcEndpoint: string

  /** Internal L1 WebSocket endpoint */
  l1RpcEndpointWebsocket?: string

  /** Internal L2 RPC endpoint */
  l2RpcEndpoint: string

  /** DA Publisher endpoint */
  daPublisherEndpoint: string

  /** Beacon RPC endpoint (for Celestia) */
  beaconRpcEndpoint?: string
}

export interface DatabaseConfig {
  /** Database admin connection for initialization */
  admin?: {
    host: string
    port: number
    username: string
    /** Use $ENV:VAR_NAME to reference environment variable */
    password: string
    database: string
    /** VPC/internal host if different from public */
    vpcHost?: string
    vpcPort?: number
  }

  /** Individual service database names (auto-created) */
  databases: {
    rollupNode: string
    bridgeHistory: string
    gasOracle: string
    coordinator: string
    chainMonitor: string
    rollupExplorer: string
    blockscout: string
    adminSystem: string
    l1Explorer?: string
  }

  /** Database user credentials per service */
  credentials: {
    /** Use $ENV:VAR_NAME pattern for secrets */
    rollupNodePassword: string
    bridgeHistoryPassword: string
    gasOraclePassword: string
    coordinatorPassword: string
    chainMonitorPassword: string
    rollupExplorerPassword: string
    blockscoutPassword: string
    adminSystemPassword: string
    l1ExplorerPassword?: string
  }
}

export interface AccountsConfig {
  /** Deployer account (deploys all contracts) */
  deployer: {
    address: string
    /** Use $ENV:VAR_NAME for private key */
    privateKey: string
  }

  /** Owner account (owns upgradeable contracts) */
  owner: {
    address: string
  }

  /** L1 commit sender (submits batch commitments) */
  l1CommitSender: {
    address: string
    privateKey: string
  }

  /** L1 finalize sender (finalizes batches) */
  l1FinalizeSender: {
    address: string
    privateKey: string
  }

  /** L1 gas oracle sender */
  l1GasOracleSender: {
    address: string
    privateKey: string
  }

  /** L2 gas oracle sender */
  l2GasOracleSender: {
    address: string
    privateKey: string
  }
}

export interface DogecoinConfig {
  /** Network type */
  network: 'mainnet' | 'testnet'

  /** RPC connection */
  rpc: {
    url: string
    username: string
    /** Use $ENV:VAR_NAME for password */
    password: string
  }

  /** Optional Blockbook API for UTXO queries */
  blockbook?: {
    apiUrl: string
    apiKey?: string
  }

  /** Block height to start indexing from */
  indexerStartHeight: number

  /** Wallet file path */
  walletPath: string
}

export interface CelestiaConfig {
  /** Tendermint RPC URL */
  tendermintRpcUrl: string

  /** DA namespace (hex) */
  namespace: string

  /** Signer address for DA submissions */
  signerAddress: string

  /** Mnemonic for Celestia account - use $ENV:VAR_NAME */
  mnemonic: string

  /** Block height to start indexing from */
  indexerStartBlock: number

  /** Auth token for Celestia node */
  authToken?: string
}

export interface BridgeConfig {
  /** Seed string for deterministic key derivation */
  seedString: string

  /** Threshold settings */
  thresholds: {
    sequencer: number
    correctness: number
    attestation: number
    recovery: number
  }

  /** Key counts */
  keyCounts: {
    correctness: number
    attestation: number
    recovery: number
  }

  /** Timelock for recovery operations (seconds) */
  timelockSeconds: number

  /** Fee rate in satoshis per kvB */
  feeRateSatPerKvb: number

  /** Target funding amounts in satoshis */
  targetAmounts: {
    sequencer: number
    feeWallet: number
    bridge: number
  }

  /** Required confirmations for deposits */
  confirmationsRequired: number

  /** Deposit and withdrawal fees in wei */
  fees: {
    deposit: string
    withdrawal: string
    minWithdrawalAmount: string
  }

  /** Fee recipient address */
  feeRecipient: string
}

export interface SigningConfig {
  /** Signing method */
  method: 'local' | 'cubesigner' | 'aws-kms'

  /** Local signer configuration */
  local?: {
    /** Signer instances */
    signers: Array<{
      index: number
      port: number
      /** Private key - use $ENV:VAR_NAME */
      privateKey?: string
    }>
  }

  /** CubeSigner configuration */
  cubesigner?: {
    /** Role configurations from cubesigner-init */
    roles: Array<{
      roleId: string
      name: string
      keys: Array<{
        keyId: string
        keyType: string
        publicKey: string
        materialId: string
      }>
    }>
  }

  /** AWS KMS configuration */
  awsKms?: {
    region: string
    accountId: string
    networkAlias: string
    /** Key suffixes for different signers */
    suffixes: string[]
  }

  /** TSO service URL */
  tsoServiceUrl?: string
}

export interface FrontendConfig {
  /** Base domain for all services */
  baseDomain: string

  /** Individual service hostnames (subdomain or full domain) */
  hosts: {
    frontend: string
    bridgeHistoryApi: string
    rollupExplorerApi: string
    coordinatorApi: string
    rpcGateway: string
    rpcGatewayWs?: string
    blockscout: string
    blockscoutBackend?: string
    adminDashboard: string
    grafana: string
    l1Explorer?: string
    l1Devnet?: string
    tso?: string
    celestia?: string
    dogecoin?: string
    blockbook?: string
  }

  /** External URLs (for user-facing links) */
  externalUrls: {
    l1Rpc: string
    l2Rpc: string
    l1Explorer: string
    l2Explorer: string
    bridgeApi: string
    rollupScanApi: string
  }

  /** WalletConnect project ID */
  walletConnectProjectId?: string
}

export interface RollupConfig {
  /** Maximum transactions per chunk */
  maxTxInChunk: number

  /** Maximum blocks per chunk */
  maxBlockInChunk: number

  /** Maximum batches per bundle */
  maxBatchInBundle: number

  /** Maximum gas limit for L1 messages */
  maxL1MessageGasLimit: number

  /** Coordinator timing */
  coordinator: {
    chunkCollectionTimeSec: number
    batchCollectionTimeSec: number
    bundleCollectionTimeSec: number
    /** JWT secret for coordinator API - use $ENV:VAR_NAME */
    jwtSecretKey: string
  }

  /** Finalization settings */
  finalization: {
    batchDeadlineSec: number
    relayMessageDeadlineSec: number
  }

  /** Verifier digests (hex) */
  verifierDigests?: {
    digest1: string
    digest2: string
  }
}

export interface ContractsConfig {
  /** Deployment salt for deterministic addresses */
  deploymentSalt: string

  /** L1 fee vault address */
  l1FeeVaultAddr: string

  /** Gas oracle settings */
  gasOracle: {
    blobScalar: number
    scalar: number
    penaltyThreshold: number
    penaltyFactor: number
  }

  /** Alternative gas token settings */
  alternativeGasToken?: {
    enabled: boolean
    tokenAddress?: string
    exchangeRateMode: 'Fixed' | 'Oracle'
    fixedExchangeRate?: string
    tokenSymbolPair?: string
  }

  /** Contract address overrides (for reusing existing contracts) */
  overrides?: {
    l2MessageQueue?: string
    l1GasPriceOracle?: string
    l2Whitelist?: string
    l2Weth?: string
    l2TxFeeVault?: string
    l1Weth?: string
    l1PlonkVerifier?: string
  }

  /** Contract verification settings */
  verification?: {
    l1VerifierType: 'blockscout' | 'etherscan'
    l2VerifierType: 'blockscout' | 'etherscan'
    l1ExplorerUri: string
    l2ExplorerUri: string
    l1ApiKey?: string
    l2ApiKey?: string
  }

  /** L1 contract deployment block (set after deployment) */
  l1DeploymentBlock?: number
}

export interface GenesisConfig {
  /** Maximum ETH supply on L2 */
  maxEthSupply: string

  /** Initial deployer balance in wei */
  deployerInitialBalance: string

  /** Base fee per gas */
  baseFeePerGas: number
}

export interface TestConfig {
  /** Enable mock finalization (skip actual L1 finalization) */
  mockFinalizeEnabled?: boolean

  /** Mock finalization timeout in seconds */
  mockFinalizeTimeoutSec?: number

  /** Skip certain validations */
  skipValidation?: boolean
}

/**
 * Container image configuration for a single service
 */
export interface ImageConfig {
  /** Container registry/repository (e.g., scrolltech/l2geth, ghcr.io/org/image) */
  repository?: string

  /** Image tag (e.g., v5.8.36, latest). Supports $ENV:VAR_NAME pattern */
  tag?: string

  /** Image pull policy */
  pullPolicy?: 'Always' | 'IfNotPresent' | 'Never'
}

/**
 * Container images configuration for all services
 * Allows overriding default images for testing/development
 */
export interface ImagesConfig {
  /** Default settings applied to all services */
  defaults?: {
    pullPolicy?: 'Always' | 'IfNotPresent' | 'Never'
  }

  /** Per-service image overrides */
  services?: {
    // L2 Execution Layer
    l2Sequencer?: ImageConfig
    l2Rpc?: ImageConfig
    l2Bootnode?: ImageConfig

    // Rollup Services
    rollupRelayer?: ImageConfig
    coordinator?: ImageConfig
    daPublisher?: ImageConfig
    gasOracle?: ImageConfig
    feeOracle?: ImageConfig
    chainMonitor?: ImageConfig

    // Bridge Services
    bridgeHistoryApi?: ImageConfig
    bridgeHistoryFetcher?: ImageConfig
    withdrawalProcessor?: ImageConfig
    tsoService?: ImageConfig
    cubesignerSigner?: ImageConfig

    // Frontend & Explorers
    frontends?: ImageConfig
    rollupExplorerBackend?: ImageConfig
    blockscout?: ImageConfig

    // Infrastructure
    l1Interface?: ImageConfig
  }
}

/**
 * Sequencer instance configuration (generated from DeploymentSpec)
 */
export interface SequencerConfig {
  index: number
  signerAddress: string
  keystore: string
  password: string
  nodeKey: string
  staticPeers: string[]
}

/**
 * Bootnode instance configuration (generated from DeploymentSpec)
 */
export interface BootnodeConfig {
  index: number
  nodeKey: string
  staticPeers: string[]
  publicPeers?: string[]
}

/**
 * Validation result
 */
export interface ValidationResult {
  valid: boolean
  errors: ValidationError[]
  warnings: ValidationWarning[]
}

export interface ValidationError {
  path: string
  message: string
  code: string
}

export interface ValidationWarning {
  path: string
  message: string
  suggestion?: string
}
